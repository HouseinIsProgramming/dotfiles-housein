-- Define a single highlight group
vim.api.nvim_set_hl(0, "HeaderGray", { fg = "#5b5b5b" })

-- Apply the single color to all header lines
local function header_color()
  local lines = {}
  for _, line_chars in ipairs(customHeaderLines) do
    local line = {
      type = "text",
      val = line_chars,
      opts = {
        hl = "HeaderGray",
        shrink_margin = false,
        position = "center",
      },
    }
    table.insert(lines, line)
  end

  local output = {
    type = "group",
    val = lines,
    opts = { position = "center" },
  }

  return output
end

